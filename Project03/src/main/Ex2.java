package main;

/*
 * 산술연산자
 * */
public class Ex2 {

	public static void main(String[] args) {
		
		System.out.println(10 + 2);  // 더하기
		System.out.println(10 - 2); // 빼기
		System.out.println(10 * 2); // 곱하기
		System.out.println(10 / 2); // 나누기해서 몫 구함
		System.out.println(10 % 2); // 나누기해서 나머지 구함
		// 2로 나눈 나머지가 0이라면 -> 10은 짝수이다

		//나머지 연산자는 짝수/홀수, 배수 판단 문제에서 자주 사용
	}
}
