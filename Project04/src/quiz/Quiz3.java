package quiz;

// for문
public class Quiz3 {

	public static void main(String[] args) {
		
		// 1. for문을 사용해 "안녕"를 5번 출력하세요
		for (int i = 1; i <= 5; i++) {
		    System.out.println("안녕");
		}

		// 2. for문을 사용해 1부터 7까지 출력하세요
		for (int i = 1; i <= 7; i++) {
		    System.out.println(i);
		}

		// 3. for문을 사용해 "나무를 1번 찍었습니다"부터
		// "나무를 10번 찍었습니다"까지 출력하세요
		for (int i = 1; i <= 10; i++) {
		    System.out.println("나무를 " + i + "번 찍었습니다");
		}

		// 4. for 사용해 5부터 10까지 출력하세요
		for (int i = 5; i <= 10; i++) {
		    System.out.println(i);
		}

	}

}
